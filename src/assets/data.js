function alphabetical(arrayOfArrays, sortByKey) {
  for (let i = 0; i < arrayOfArrays.length; i++) {
    arrayOfArrays[i].sort((a, b) => {
      if (a[sortByKey] < b[sortByKey]) {
        return -1;
      }
      if (a[sortByKey] > b[sortByKey]) {
        return 1;
      }
      return 0;
    });
  }
  return arrayOfArrays;
}
